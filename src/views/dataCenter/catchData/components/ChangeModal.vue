<template>
    <div class="container">
      <span @click="toggleModal">
        <slot></slot>
      </span>
      <el-dialog title="变更证件" :visible.sync="showModal">
        <el-row>
          <el-col :span="20" :offset="2">
            <el-form :model="formData" :rules="rules" ref="ruleForm">
              <el-input type="hidden" :value="id"></el-input>
              <el-form-item label="原证件号" label-width="100px">
                <el-input :value="oldIdentityNo" readonly></el-input>
              </el-form-item>
              <el-form-item label="新证件号" label-width="100px" prop="newIdentityNo">
                <el-input v-model="formData.newIdentityNo"></el-input>
              </el-form-item>
            </el-form>
          </el-col>
        </el-row>
        <div slot="footer" class="dialog-footer">
          <el-button @click="showModal = false">取 消</el-button>
          <el-button type="primary" @click="okHandle('ruleForm')">确 定</el-button>
        </div>
      </el-dialog>
    </div>
</template>

<script>
export default {
  name: "ChangeModal",
  props: ["id", "oldIdentityNo"],
  data() {
    return {
      showModal: false,
      formData: {},
      rules: {
        newIdentityNo: [{ required: true, message: "请输入证件号", trigger: "blur" }]
      }
    };
  },
  methods: {
    toggleModal() {
      this.showModal = !this.showModal;
    },
    okHandle(formName) {
      this.$refs[formName].validate(valid => {
        if (valid) {
          let data = { ...this.formData, id: this.id, oldIdentityNo: this.oldIdentityNo }
          this.$emit("save", data);
          this.showModal = false;
        } else {
          return false;
        }
      });
    }
  }
};
</script>

<style scoped>
.container {
  display: inline-block;
}
</style>
